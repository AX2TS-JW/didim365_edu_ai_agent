def load():
    if es_client is None:
        print("ES 연결 실패. .env의 ES_URL을 확인하세요.")
        return

    ensure_indices()

    actions = []
    for name, code in REGION_CODES.items():
        city = CITY_MAP.get(code[:2], "기타")
        # 괄호가 있으면 짧은 이름도 aliases에 추가 (예: "강서구(부산)" → "강서구")
        short_name = name.split("(")[0] if "(" in name else ""
        aliases = f"{name} {short_name} {city}{name}".strip()

        doc_id = f"{code}_{name}"  # 중복 방지용 고유 ID
        actions.append({"index": {"_index": IDX_REGION_CODES, "_id": doc_id}})
        actions.append({
            "region_name": name,
            "region_code": code,
            "city": city,
            "aliases": aliases,
        })

    try:
        result = es_client.bulk(body=actions, refresh="wait_for")
        errors = result.get("errors", False)
        items = result.get("items", [])
        print(f"적재 완료: {len(items)}건, 오류: {errors}")
    except Exception as e:
        print(f"벌크 적재 실패: {e}")


if __name__ == "__main__":
    load()
