def load():
    if es_client is None:
        print("ES 연결 실패. .env의 ES_URL을 확인하세요.")
        return

    ensure_indices()

    # 현재 ES에 있는 문서 ID 조회 (중복 방지)
    existing_ids = set()
    try:
        result = es_client.search(
            index=IDX_REGION_CODES,
            body={"query": {"match_all": {}}, "_source": False},
            size=1000,
        )
        existing_ids = {hit["_id"] for hit in result["hits"]["hits"]}
        print(f"기존 적재 건수: {len(existing_ids)}")
    except Exception as e:
        print(f"기존 데이터 조회 실패 (첫 실행이면 정상): {e}")

    actions = []
    skipped = 0
    for name, code in REGION_CODES.items():
        doc_id = f"{code}_{name}"
        if doc_id in existing_ids:
            skipped += 1
            continue

        city = CITY_MAP.get(code[:2], "기타")
        short_name = name.split("(")[0] if "(" in name else ""
        # 경기도 구 단위는 짧은 이름도 추가 (예: "성남시분당구" → "분당구")
        extra_alias = ""
        for suffix in ["구", "시", "군"]:
            idx = name.rfind(suffix)
            if idx > 0 and name[:idx] != name:
                # "성남시분당구" → "분당구"
                for i in range(len(name) - 1, -1, -1):
                    if name[i] in "시도":
                        extra_alias = name[i+1:]
                        break
                break

        aliases = f"{name} {short_name} {extra_alias} {city}{name}".strip()

        actions.append({"index": {"_index": IDX_REGION_CODES, "_id": doc_id}})
        actions.append({
            "region_name": name,
            "region_code": code,
            "city": city,
            "aliases": aliases,
        })

    if not actions:
        print(f"신규 적재 대상 없음 (기존 {len(existing_ids)}건, 스킵 {skipped}건)")
        return

    try:
        result = es_client.bulk(body=actions, refresh="wait_for")
        errors = result.get("errors", False)
        new_count = len(actions) // 2
        print(f"신규 적재: {new_count}건, 스킵(중복): {skipped}건, 오류: {errors}")
    except Exception as e:
        print(f"벌크 적재 실패: {e}")


if __name__ == "__main__":
    load()
